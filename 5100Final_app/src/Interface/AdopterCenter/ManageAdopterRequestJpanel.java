    public void populate(){
        int rowCount = adopterTable.getRowCount();
        DefaultTableModel dtm = (DefaultTableModel)adopterTable.getModel();
        jScrollPane1.getViewport().setBackground(new Color(249,246,246));
        for(int i = rowCount - 1; i >=0; i--) {
            dtm.removeRow(i);
        }
        
        for(Adopter adopter : adoptercenter.getAdopterDir().getAdopterDirectory()){
            Object row[] = new Object[dtm.getColumnCount()];
            row[0] = adopter;
            row[1] = adopter.getUserInfo().getEmail();
            row[2] = adopter.getUserInfo().getSalary();
            row[3] = adopter.getUserInfo().getTypeList();
            row[4] = adopter.getStatus();
            
            dtm.addRow(row);
        }
    }

