    private void AcceptBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AcceptBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = adopterTable.getSelectedRow();
        
        if(selectedRow >= 0)
        {
            Adopter adop = (Adopter)adopterTable.getValueAt(selectedRow, 0);
            adop.setStatus("Permitted");
            adop.getAnimalDir().addStrayAnimal(strayanimal);
            JOptionPane.showMessageDialog(null, "Change Status Sucessfully!");
        }
        
        else
        {
            JOptionPane.showMessageDialog(null, "Please select a Row!!");
        }
        
        populate();
        
    }//GEN-LAST:event_AcceptBtnActionPerformed

