    private void RejectBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RejectBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = ApplicationTable.getSelectedRow();
        
        if(selectedRow >= 0)
        {      
            Request nowRequest = (Request)ApplicationTable.getValueAt(selectedRow, 1);
            this.res.getRequestDir().deleteRequest(nowRequest);
            JOptionPane.showMessageDialog(null, "Delete Request Successfully!");
        }
        
        else
        {
            JOptionPane.showMessageDialog(null, "Please select a Row!!");
        }
        
        populate();
    }//GEN-LAST:event_RejectBtnActionPerformed

    private void AgreeBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AgreeBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = ApplicationTable.getSelectedRow();
        
        if(selectedRow >= 0)
        {      
            Adopter nowAdopter = (Adopter)ApplicationTable.getValueAt(selectedRow, 0);
            StrayAnimal nowAnimal = (StrayAnimal) ApplicationTable.getValueAt(selectedRow, 3);
            Request nowH = (Request)ApplicationTable.getValueAt(selectedRow, 1);
            if(this.res.getStrayanimalDir().getStrayAnimalDirectory().contains(nowAnimal)){
                nowAdopter.getAnimalDir().addStrayAnimal(nowAnimal);
                this.res.getRequestDir().deleteRequest(nowH);
                this.res.getStrayanimalDir().deleteStrayAnimal(nowAnimal);
                JOptionPane.showMessageDialog(null, "Confirm Request Successfully!");
            }else{
                this.res.getRequestDir().deleteRequest(nowH);
                JOptionPane.showMessageDialog(null, "This animal donnot exist in this rescue center, maybe it has been deleted or adopted!");
            }
            
        }
        
        else
        {
            JOptionPane.showMessageDialog(null, "Please select a Row!!");
        }
        
        populate();
    }//GEN-LAST:event_AgreeBtnActionPerformed

