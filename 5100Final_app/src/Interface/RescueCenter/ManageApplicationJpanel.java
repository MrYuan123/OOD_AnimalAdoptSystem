    public void populate()
    {
        int rowCount = ApplicationTable.getRowCount();
        DefaultTableModel dtm = (DefaultTableModel)ApplicationTable.getModel();
        jScrollPane1.getViewport().setBackground(new Color(249,246,246));
        for(int i = rowCount - 1; i >= 0; i--)
        {
            dtm.removeRow(i);
        }
        
        for(Request temp : this.res.getRequestDir().getRequestList()){
           Object row[] = new Object[dtm.getColumnCount()]; 
           row[0] = temp.getAdopter();
           row[1] = temp;
           row[2] = temp.getStrayanimal().getSpecies();
           row[3] = temp.getStrayanimal();
           row[4] = temp.getStrayanimal().getStatus();
           dtm.addRow(row);
        }
    }

