    private void EditBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = EmployeeTbl.getSelectedRow();
        
        if(selectedRow >= 0)
        {
            Account acc = (Account)EmployeeTbl.getValueAt(selectedRow, 0);
            SaveBtn.setEnabled(true);
            UserNameTxt.setText(acc.getName());
            PasswordTxt.setText(acc.getPassword());
            RepasswordTxt.setText(acc.getPassword());
            CreateBtn.setEnabled(false);
        }
        
        else
        {
            JOptionPane.showMessageDialog(null, "Please select a Row!!");
        }
    }//GEN-LAST:event_EditBtnActionPerformed

    private void CreateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CreateBtnActionPerformed
        // TODO add your handling code here:
        if(UserNameTxt.getText().isEmpty() || PasswordTxt.getText().isEmpty() || RepasswordTxt.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Some input text is empty!");
            UserNameTxt.setText("");
            PasswordTxt.setText("");
            RepasswordTxt.setText("");
            return;
        }
        
        if(!this.ecoSystem.newName(UserNameTxt.getText())){
            JOptionPane.showMessageDialog(null, "User name has existed!");
            return;
        }
        
        if(RepasswordTxt.getText().equalsIgnoreCase(PasswordTxt.getText())){
            this.account = this.res.getEmployeeaccountDir().createAccount();
            account.setName(UserNameTxt.getText());
            account.setPassword(PasswordTxt.getText());
            System.out.println(account.getName());
            System.out.println(res.getName());
            
        }
            else{
            JOptionPane.showMessageDialog(null, "Something Wrong, Please Check!");
            return;
        }
        JOptionPane.showMessageDialog(null, "Create Rescue Center Employee Successfully!");
        
        UserNameTxt.setText("");
        PasswordTxt.setText("");
        RepasswordTxt.setText("");
        populate();
    }//GEN-LAST:event_CreateBtnActionPerformed

    private void DeleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteBtnActionPerformed
        // TODO add your handling code here:
        int row = EmployeeTbl.getSelectedRow();
        
        if(row < 0)
        {
            JOptionPane.showMessageDialog(null, "Please select any row!!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        Account a = (Account)EmployeeTbl.getValueAt(row, 0);
        res.getEmployeeaccountDir().deleteAccount(a);
        populate();
    }//GEN-LAST:event_DeleteBtnActionPerformed

    private void SaveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveBtnActionPerformed
        // TODO add your handling code here:

        if(UserNameTxt.getText().isEmpty() || PasswordTxt.getText().isEmpty() || RepasswordTxt.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Some input text is empty!");
            UserNameTxt.setText("");
            PasswordTxt.setText("");
            RepasswordTxt.setText("");
            return;
        }
        
        if(RepasswordTxt.getText().equalsIgnoreCase(PasswordTxt.getText())){
            int selectedRow = EmployeeTbl.getSelectedRow();
        
        if(selectedRow >= 0)
        {
            Account acc = (Account)EmployeeTbl.getValueAt(selectedRow, 0);
            if(!this.ecoSystem.newName(UserNameTxt.getText())&& !UserNameTxt.getText().equals(acc.getName())){
                    JOptionPane.showMessageDialog(null, "User name has existed!");
                    return;
            }
            acc.setName(UserNameTxt.getText());
            acc.setPassword(PasswordTxt.getText());
            System.out.println(acc.getName());
            System.out.println(res.getName());
            
        }
        }
            else{
            JOptionPane.showMessageDialog(null, "Something Wrong, Please Check!");
            return;
        }
        JOptionPane.showMessageDialog(null, "Update Rescue Center Employee Successfully!");
        
        UserNameTxt.setText("");
        PasswordTxt.setText("");
        RepasswordTxt.setText("");
        SaveBtn.setEnabled(false);
        CreateBtn.setEnabled(true);
        populate();
    }//GEN-LAST:event_SaveBtnActionPerformed


