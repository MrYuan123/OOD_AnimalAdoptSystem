    public void populate(){
        int rowCount = EmployeeTbl.getRowCount();
        DefaultTableModel dtm = (DefaultTableModel)EmployeeTbl.getModel();
        jScrollPane1.getViewport().setBackground(new Color(249,246,246));
        for(int i = rowCount - 1; i >=0; i--) {
            dtm.removeRow(i);
        }
        
        for(Account a  : this.res.getEmployeeaccountDir().getAccountList()){
            Object row[] = new Object[dtm.getColumnCount()];
            row[0] = a;
            row[1] = a.getPassword();                      
            dtm.addRow(row);
        }
    }

