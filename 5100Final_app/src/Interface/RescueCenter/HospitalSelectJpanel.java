    private void SendBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SendBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = SelectTable.getSelectedRow();
        
        if(selectedRow >= 0)
        {
            PetHospital nowH = (PetHospital)SelectTable.getValueAt(selectedRow, 0);
            if(nowH.getStrayanimalDir().getStrayAnimalDirectory().size()>=nowH.getCapacity()){
                JOptionPane.showMessageDialog(null, "This hospital is full, please select another hospital!");
                return;
            }
            if(!strayanimal.getTraceStatus().equals("available")){
                JOptionPane.showMessageDialog(null, "This animal is not at hospital right now!");
                return;
            }
            
            strayanimal.setTraceStatus("waiting");
            nowH.getStrayanimalDir().addStrayAnimal(strayanimal);
            JOptionPane.showMessageDialog(null, "Send Stray Animal Successfully!");
        }
        
        else
        {
            JOptionPane.showMessageDialog(null, "Please select a Row!!");
        }
        
    }//GEN-LAST:event_SendBtnActionPerformed

