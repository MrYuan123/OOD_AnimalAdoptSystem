    public void populate(){
        int rowCount = SelectTable.getRowCount();
        DefaultTableModel dtm = (DefaultTableModel)SelectTable.getModel();
        jScrollPane1.getViewport().setBackground(new Color(249,246,246));
        for(int i = rowCount - 1; i >=0; i--) {
            dtm.removeRow(i);
        }
        for(Network nw: this.ecoSystem.getNetworkDir().getNetworkList()){
            
        for(PetHospital ph : nw.getEnterpriseDir().getPethospitalDir().getPetHospitalList()){
            Object row[] = new Object[dtm.getColumnCount()];
            row[0] = ph;
            row[1] = ph.getCapacity();
            row[3] = ph.getSuccessRate();
            row[4] = ph.getAddress();
            
            dtm.addRow(row);
        }
        }
    }

