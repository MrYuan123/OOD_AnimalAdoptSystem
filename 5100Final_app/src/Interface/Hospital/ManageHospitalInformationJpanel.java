    private void CreateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CreateBtnActionPerformed
        // TODO add your handling code here:
        if(CapacityTxt.getText().isEmpty() || RateTxt.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Some input text is empty!");
            CapacityTxt.setText("");
            RateTxt.setText("");
            return;
        }
        
        int capacity;
        try
        {
            capacity = Integer.parseInt(CapacityTxt.getText());
            if(capacity < 0){
                JOptionPane.showMessageDialog(null, "Hospital capacity should be no negative integer!");
                return;
            }
        }
        
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, "Hospital capacity should be integer!");
            return;
        }
        
        int successrate;
        try
        {
            successrate = Integer.parseInt(RateTxt.getText());
            if(successrate <0 || successrate > 100){
                JOptionPane.showMessageDialog(null, "Success rate is between 0 and 100.");
                return;
            }
        }
        
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, "Hospital success rate should be integer!");
            return;
        }
        
        petHospital.setCapacity(capacity);
        petHospital.setSuccessRate(successrate);
        
        JOptionPane.showMessageDialog(null, "Create Pet Hospital Successful!");
        
//        CapacityTxt.setText("");
//        RateTxt.setText("");
        
        populate();
    }//GEN-LAST:event_CreateBtnActionPerformed

