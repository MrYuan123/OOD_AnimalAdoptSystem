    public void populate()
    {
        int rowCount = InformationJtable.getRowCount();
        DefaultTableModel dtm = (DefaultTableModel)InformationJtable.getModel();
        jScrollPane1.getViewport().setBackground(new Color(249,246,246));
        for(int i = rowCount - 1; i >= 0; i--)
        {
            dtm.removeRow(i);
        }
        
        Object row[] = new Object[dtm.getColumnCount()];
        row[0] = petHospital;
        row[1] = petHospital.getCapacity();
        row[2] = petHospital.getSuccessRate();
        dtm.addRow(row);
    }

