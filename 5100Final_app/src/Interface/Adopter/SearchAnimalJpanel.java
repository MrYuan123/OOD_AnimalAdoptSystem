    public void populate()
    {
        int rowCount = animalTbl.getRowCount();
        DefaultTableModel dtm = (DefaultTableModel)animalTbl.getModel();
        jScrollPane1.getViewport().setBackground(new Color(249,246,246));
        for(int i = rowCount - 1; i >= 0; i--)
        {
            dtm.removeRow(i);
        }
        
        for(Network network : this.ecoSystem.getNetworkDir().getNetworkList())
        {
            for(RescueCenter rescueCenter : network.getEnterpriseDir().getRescuecenterDir().getRcd())
            {
                for(StrayAnimal strayAnimal : rescueCenter.getStrayanimalDir().getStrayAnimalDirectory())
                {
                    Object row[] = new Object[dtm.getColumnCount()];
                    row[0] = rescueCenter;
                    row[1] = strayAnimal;
                    row[2] = strayAnimal.getSpecies();
                    row[3] = strayAnimal.getDescription();
                    row[4] = strayAnimal.getStatus();
                    dtm.addRow(row);
                }
            }
        }
    }

