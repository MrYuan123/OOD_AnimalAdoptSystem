    private void AdoptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AdoptActionPerformed
        // TODO add your handling code here:
        int selectedRow = animalTbl.getSelectedRow();
        
        if(selectedRow >= 0){
            
            RescueCenter rescueCenter = (RescueCenter)animalTbl.getValueAt(selectedRow, 0); 
            StrayAnimal strayAnimal = (StrayAnimal)animalTbl.getValueAt(selectedRow, 1);
            if(strayAnimal.getStatus().equals("Healthy")){
                rescueCenter.getRequestDir().addRequest(strayAnimal, this.adopter);
                JOptionPane.showMessageDialog(null, "Send Request Successfully!");
                return;
            }else{
                JOptionPane.showMessageDialog(null, "this animal is not at healthy condition, so cannot be adopted!");
                return;
            }
            
        }
        
        else
        {
            JOptionPane.showMessageDialog(null, "Please select a Row!!");
        }
    }//GEN-LAST:event_AdoptActionPerformed

